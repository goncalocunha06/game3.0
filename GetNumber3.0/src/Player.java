
public class Player {
	
	private String name;
	private Integer number;
	
	public Player (String playerName) {
		this.name = playerName;
	}

	public String getName() {
		return name;
	}
	

	public void setName(String name) {
		this.name = name;
	}

	public Integer getNumber() {
		return number;
	}

	public void setNumber(Integer number) {
		this.number = number;
	}	
	
}
